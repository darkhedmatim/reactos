<h1>Help &amp; FAQ </h1> 
<h2>Why we offer this service? </h2>
<p>People around the ReactOS project often ask for a technology how to share text content (code, debug logs, urls, etc.).  </p>
<p>The ReactOS &quot;Copy&amp;Paste&quot; paste service allows people to paste chunks of code for others to view. This is useful for situations such as asking for programming help on IRC, where it is frowned upon to paste chunks of code to the channel or to individual. With this paste service, the user pastes his or her code to the site and is given a url to provide to others so they can find the code. </p>
<h2>What's different about this one? </h2>
<p> A lot of paste sites have appeared lately, and many of them are quite similar. The ReactOS &quot;Copy&amp;Paste&quot; paste service provide a unique list of features and a broad range of code highlight support. It uses <a href="http://rafb.net/paste/highlight.phps" target="_blank">rafb.net's code highlight php library</a> which is free available under BSD license. </p>
<h2>How long is the paste accessable? </h2>
<p> The paste is accessable for 7 days. </p>
