<h3>RosCMS Rules</h3>
<ul>
  <li>Do NOT polute the database tables with entries! So do NOT add new page / 
    content entries with no content!</li>
  <li>Do NOT add spam, bad content, etc.!</li>
  <li>Do NOT start a content &quot;war&quot;!</li>
  <li>Translate the content from the original &quot;english&quot; version to your 
    language. Do NOT do it the other way round!</li>
  <li>If you made a mistake, always inform an admin member!</li>
  <li>If you think someone else know your account data (login data), change your 
    password immediately (and inform an admin member)!</li>
  <li><em>To be continued! [...]</em></li>
</ul>
<p>If you do NOT follow the rules, a member of the admin group will delete your 
  translator group memebership and/or ban/kick your account!</p>

<h2>General</h2>
  <p align="left">Use the &quot;filter &amp; sort&quot; function to browse through 
    the database entries.<br>
    Use the &quot;action&quot; buttons to edit, delete, etc. a specific item.<br>
    The &quot;info&quot; icons indicate the current state (active, visbible and 
    locked).<br>
    Locked means that the items with the &quot;locked&quot; icons can not be altered 
    by the current user. You need a higher account level if you want to edit such 
    a item.</p>
